package com.aimobs.entity.ai.core;

/**
 * Enumeration of status icons for visual feedback display.
 * Each icon represents a different type of activity or state.
 */
public enum StatusIcon {
    /**
     * Icon representing movement/navigation activity.
     */
    MOVEMENT,
    
    /**
     * Icon representing combat/attack activity.
     */
    COMBAT,
    
    /**
     * Icon representing item collection activity.
     */
    COLLECTION,
    
    /**
     * Icon representing defensive/guarding activity.
     */
    DEFENSE,
    
    /**
     * Icon representing idle/waiting state.
     */
    IDLE,
    
    /**
     * Icon representing error/failure state.
     */
    ERROR,
    
    /**
     * Icon representing processing/thinking state.
     */
    PROCESSING
}